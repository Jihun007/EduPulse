-- 테이블 재생성을 위해 DROP
DROP TABLE IF EXISTS CODE;
DROP TABLE IF EXISTS QITEM;
DROP TABLE IF EXISTS DGSTFN;
DROP TABLE IF EXISTS CODE_QITEM;

-- 테이블 재생성
-- 코드
CREATE TABLE CODE (
	CODE_ID TEXT NOT NULL,		-- 코드번호
	CODE_VALUE NUMBER NOT NULL,	-- 코드값
	CODE_NM TEXT,				-- 코드명
	PRIMARY KEY (CODE_ID, CODE_VALUE)
);

-- 문항
CREATE TABLE QITEM (
	QITEM_NUM TEXT NOT NULL,	-- 문항번호
	QITEM_NM TEXT,				-- 문항명
	PRIMARY KEY (QITEM_NUM)
);

-- 코드문항
CREATE TABLE CODE_QITEM (
	CODE_ID TEXT NOT NULL,		-- 코드번호
	QITEM_NUM TEXT NOT NULL,	-- 문항번호
	FOREIGN KEY (CODE_ID) REFERENCES CODE (CODE_ID)
	FOREIGN KEY (QITEM_NUM) REFERENCES QITEM (QITEM_NUM)
);

-- 만족도
CREATE TABLE DGSTFN (
	RSPDNT_ID INTEGER PRIMARY KEY AUTOINCREMENT,	-- 응답자ID
	RSPDNT_DATE TEXT,			-- 응답자 일자
	Q1 INTEGER,
	Q2 INTEGER,
	Q3 INTEGER,
	Q4 INTEGER,
	Q5 INTEGER,
	Q6 TEXT,
	Q7 TEXT,
	Q8 INTEGER,
	Q9 INTEGER,
	Q10 INTEGER,
	Q11 INTEGER,
	Q12 INTEGER,
	Q13 INTEGER
);

